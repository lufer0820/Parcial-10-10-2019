public class Parcial {
    //atributos
    private int id;
    private String fecha;
    private String nombre;
    
    //metodos
     public int getId() {
        return id;
    }
     
    public void setId(int id) {
        
        this.id = id;
    } 

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    
    }
    

